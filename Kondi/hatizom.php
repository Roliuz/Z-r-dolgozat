                    <p class="text-black m-2 mb-4" style="font-family: Franklin Gothic Medium;"> Egy másféle módon is tudod végezni a gyakorlatot, a tenyered kifelé fordítva egy teljesen más hatást fog eredményezni. A felső, csúcsösszehúzódási pontban próbáld meg a könyökeidet legalább gondolatban egymás felé közelíteni a hátad mögött, kvázi összepréselve a hátizmokat. Ez segíteni fog az izomérzet kialakulásában.
