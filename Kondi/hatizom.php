                    <ol>
                        <li class="text-black m-1" style="font-family: Franklin Gothic Medium;">A lábad legyen kb. vállszélességnyire terpeszben, és hajolj előre minimum 45 fokos szögig, de ha bírod derékkal, akkor a törzsed vízszintes pozíciójáig is előrehajolhatsz. Hajlítsd be kissé a térdeidet, hogy a hát alsó részében lévő szükségtelen feszültség eltűnjön. Ragadd meg a rudat vállszélességben úgy, hogy a tenyered befelé nézzen. A karodnak egyenesen kell lógnia a mozgás kezdetekor.
                        </li>
                        <li class="text-black m-1" style="font-family: Franklin Gothic Medium;">Húzd fel a rudat a hasad/mellkasod irányába oly módon, hogy a könyöködre koncentrálsz a mozdulat egész ideje alatt. Ez fog segíteni abban, hogy kikapcsold a mozgásból a bicepszedet amennyire lehet. Amikor a rúd megérintette a törzsedet, lassan engedd vissza a kiinduló pozícióba. Semmiképpen se segíts e a törzseddel a mozgásba - ez a hatékonyságot is csökkenti, ellenben a sérülésveszélyt jelentősen megnöveli. inkább csökkentsd a súlyt, ha másképp nem megy.
                        </li>
                    </ol>

